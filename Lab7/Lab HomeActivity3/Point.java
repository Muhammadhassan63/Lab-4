
package lab7_homeact3;

public class Point {
    private int x;
    private int y;

    public Point(int x,int y){
        this.x = x;
        this.y = y;
    }

    public void setX_cord(int x) {
        this.x = x;
    }

    public void setY_cord(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
    


    

